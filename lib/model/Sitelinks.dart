class Sitelinks {
  final String wikipediaLink;
  final String commonsLink;
  final String wikidataLink;

  Sitelinks(this.wikipediaLink, this.commonsLink, this.wikidataLink);

  String getWikipediaLink() {
    return wikipediaLink;
  }

  String getCommonsLink() {
    return commonsLink;
  }

  String getWikidataLink() {
    return wikidataLink;
  }
}
